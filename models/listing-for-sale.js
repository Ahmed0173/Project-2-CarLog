const mongoose = require("mongoose");

const yourCarSchema = new mongoose.Schema({
    make: {
        type: String,
        required: true,
    },
    model: {
        type: String,
        required: true,
    },
    year: {
        type: Number,
        required: true,
    },
    price: {
        type: Number,
        required: true,
    },
    description: {
        type: String,
        required: true,
    },
    image: {
        type: String,
        required: true,
    },
});

const CarsForSale = mongoose.model("CarsForSale", yourCarSchema);
module.exports = CarsForSale;